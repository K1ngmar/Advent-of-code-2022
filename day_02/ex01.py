score_lookup = {
	'A': 1, 
	'B': 2,
	'C': 3
	}
# A = rock
# B = paper
# C = scissors
wining_move_lookup = {
	'C': 'A',
	'A': 'B',
	'B': 'C'
	}
losing_move_lookup = {
	'A': 'C',
	'B': 'A',
	'C': 'B'
	}

score = 0
for match in open("input").read().splitlines():
	opponent_move, my_move = match.split()
	if my_move == 'Z':
		my_move = wining_move_lookup.get(opponent_move)
		score += 6
	elif my_move == 'Y':
		my_move = opponent_move
		score += 3
	else:
		my_move = losing_move_lookup.get(opponent_move)
	score += score_lookup.get(my_move)

print("ex01:", score)